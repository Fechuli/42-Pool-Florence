char	*ft_strupcase(char *str)
{
	int	f;

	f = 0;
	while (str[f])
	{
		if ((str[f] >= 'a') && (str[f] <= 'z'))
			str[f] = ((str[f] - 'a') + 'A');
		f++;
	}
	return (str);
}
